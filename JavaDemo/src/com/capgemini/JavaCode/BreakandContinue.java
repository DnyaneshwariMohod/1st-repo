//Java program by using break and continue
package com.capgemini.JavaCode;

public class BreakandContinue {
	public static void main(String[] args) 
	{	
         for(int i=1; i<=10; i++)
         {
        	 if(i == 1)
        	 {
        		 continue;   // break;
        	 }
        	 System.out.println(i);
         }
	}


}
